package main

import (
	"github.com/grrrben/gpio"
	"github.com/stianeikeland/go-rpio/v4"
	"time"
)

func main() {

	if err := rpio.Open(); err != nil {
		panic(err)
	}

	register, err := gpio.NewShiftRegister(9, 10, 11)
	if err != nil {
		panic(err)
	}

	sequence := []uint8{0xff, 0x00, 0xff, 0x00, 0xff, 0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x01, 0x03, 0x07, 0x0f, 0x1f, 0x3f, 0x7f, 0xff, 0x01, 0x05, 0x15, 0x55, 0xb5, 0xf5, 0xfb, 0xff, 0x02, 0x03, 0x0b, 0x0f, 0x2f, 0x3f, 0xbf, 0xff}

	for _, l := range sequence {
		register.SendData(uint8(l))
		time.Sleep(time.Millisecond * 250)
	}
}
